    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked

        int fila = table.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione un registro");
        } else {
            idcodigo = Integer.parseInt((String) table.getValueAt(fila, 0).toString());
            String Nombre = (String) table.getValueAt(fila, 1);
            String Apellido = (String) table.getValueAt(fila, 2);
            int Salario = Integer.parseInt((String) table.getValueAt(fila, 3).toString());

            txtid.setText("" + idcodigo);
            txtnombre.setText(Nombre);
            txtapellido.setText(Apellido);
            txtsalario.setText("" + Salario);
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_tableMouseClicked

