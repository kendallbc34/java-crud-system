    public void modificar() {
        String id = txtid.getText();
        String Nombre = txtnombre.getText();
        String Apellido = txtapellido.getText();
        String Salario = txtsalario.getText();

        try {
            if (id.equals("") || Nombre.equals("") || Apellido.equals("") || Salario.equals("")) {
                JOptionPane.showMessageDialog(null, "Revise los datos ");
                
            }else{
                
                String sql= "UPDATE salarios SET nombre='"+Nombre+"',apellido='"+Apellido+"',"
                        + " salario='" + Salario + "' WHERE id=" + id;
                conect = con.getConnection();
                st = conect.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null,"modificado con exito");
                LimpiarTabla();
                consultar();
                
            }
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null,"Error");
        }

    }
    
    public void eliminar(){
        String id=txtid.getText();
        
        try{
            
            if(id.equals("")){
                JOptionPane.showMessageDialog(null,"Seleccione un registro");
            }
            else{
                String sql="DELETE FROM salarios WHERE id="+id;
                 conect = con.getConnection();
                st = conect.createStatement();
                st.executeUpdate(sql);
                
                JOptionPane.showMessageDialog(null,"Registro eliminado con exito");
                LimpiarTabla();
                consultar();
                
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

