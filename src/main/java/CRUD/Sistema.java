    public void consultar() {
        String sql = "SELECT * FROM salarios";
        try {
            conect = con.getConnection();
            st = conect.createStatement();
            rs = st.executeQuery(sql);

            Object[] datos = new Object[4];
            modelo = (DefaultTableModel) table.getModel();
            while (rs.next()) {
                datos[0] = rs.getInt("id");
                datos[1] = rs.getString("nombre");
                datos[2] = rs.getString("apellido");
                datos[3] = rs.getInt("Salario");

                modelo.addRow(datos);
            }
            table.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Revise los datos ");
        }

    }

    public void agregar() {

        String Nombre = txtnombre.getText();
        String Apellido = txtapellido.getText();
        String Salario = txtsalario.getText();

        try {
            if (Nombre.equals("") || Apellido.equals("") || Salario.equals("")) {
                JOptionPane.showMessageDialog(null, "Revise los datos ");
                LimpiarTabla();
            } else {
                String sql = "INSERT INTO salarios(nombre,apellido,salario) values ('" + Nombre + "','" + Apellido + "','" + Salario + "')";
                conect = con.getConnection();
                st = conect.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Exito");
                LimpiarTabla();

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    public void LimpiarTabla() {
        for (int i = 0; i < table.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }
